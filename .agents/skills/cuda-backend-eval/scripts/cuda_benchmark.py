def _parse_tensor_tile(rows: int, cols: int, inner: int) -> dict[str, int]:
    if rows <= 0 or cols <= 0 or inner <= 0:
        raise ValueError("tensor rows, cols, and inner must be positive")
    return {"rows": rows, "cols": cols, "inner": inner}


def _validate_tensor_core_tile(tensor_tile: dict[str, int]) -> None:
    if (
        tensor_tile["rows"] % 16 != 0
        or tensor_tile["cols"] % 16 != 0
        or tensor_tile["inner"] <= 0
        or tensor_tile["inner"] % 8 != 0
    ):
        raise ValueError(
            "pto_persistent_dag_tensor_core requires tensor rows/cols in multiples of 16 "
            "and tensor-inner divisible by 8"
        )


def _parse_command_examples(values: list[str] | None) -> dict[str, str]:
    examples: dict[str, str] = {}
    for value in values or ():
        if "=" not in value:
            raise ValueError(f"invalid --command-example {value!r}; expected KEY=COMMAND")
        key, command = value.split("=", 1)
        key = key.strip()
        if not key:
            raise ValueError(f"invalid --command-example {value!r}; expected KEY=COMMAND")
        examples[key] = command
    return examples


