def merge_payloads(
    payloads: list[dict[str, Any]],
    label: str,
    command_examples: dict[str, str] | None = None,
) -> dict[str, Any]:
    source_labels = [payload.get("metadata", {}).get("label", "unknown") for payload in payloads]
    git_commits = sorted(
        {
            payload.get("metadata", {}).get("git_commit", "unknown")
            for payload in payloads
            if payload.get("metadata", {}).get("git_commit")
        }
    )
    results: list[dict[str, Any]] = []
    for payload in payloads:
        results.extend(payload.get("results", []))
    tensor_tiles = [
        payload.get("metadata", {}).get("tensor_tile")
        for payload in payloads
        if payload.get("metadata", {}).get("tensor_tile") is not None
    ]
    common_tensor_tile = None
    if tensor_tiles and all(tensor_tile == tensor_tiles[0] for tensor_tile in tensor_tiles):
        common_tensor_tile = tensor_tiles[0]
    stream_pool_sizes = [
        payload.get("metadata", {}).get("stream_pool_size")
        for payload in payloads
        if payload.get("metadata", {}).get("stream_pool_size") is not None
    ]
    common_stream_pool_size = None
    if stream_pool_sizes and all(size == stream_pool_sizes[0] for size in stream_pool_sizes):
        common_stream_pool_size = stream_pool_sizes[0]

    metadata: dict[str, Any] = {
        "label": label,
        "git_commit": ",".join(git_commits) if git_commits else "unknown",
        "git_commits": git_commits,
        "machine": "combined",
        "nvidia_smi": "combined report",
        "paper_setup": (
            "Combined microbenchmark slice inspired by VDCores/MPK "
            "persistent-kernel evaluation; not an end-to-end LLM serving "
            "result."
        ),
        "source_papers": list(SOURCE_PAPERS),
        "source_labels": source_labels,
        "timestamp_unix": time.time(),
    }
    if common_tensor_tile is not None:
        metadata["tensor_tile"] = common_tensor_tile
    if common_stream_pool_size is not None:
        metadata["stream_pool_size"] = common_stream_pool_size
    if command_examples:
        metadata["command_examples"] = dict(command_examples)

    return {
        "metadata": metadata,
        "results": results,
    }


