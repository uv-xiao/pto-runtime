def _compile_slow_ptx(work_dir: Path, arch: str) -> tuple[bytes, str]:
    nvcc = _find_nvcc()
    if nvcc is None:
        return _FALLBACK_SLOW_VECTOR_ADD_PTX, "embedded-sm80-slow-ptx"

    kernel_src = work_dir / "slow_vector_add.cu"
    kernel_src.write_text(
        """
extern "C" __global__ void pto_vector_add_f32(
    const float *a, const float *b, float *out, unsigned long long n) {
    unsigned long long start = clock64();
    while (clock64() - start < 80000000ULL) {
    }
    unsigned long long i = blockIdx.x * blockDim.x + threadIdx.x;
    if (i < n) {
        out[i] = a[i] + b[i];
    }
}
""".lstrip()
    )
    ptx_path = work_dir / "slow_vector_add.ptx"
    subprocess.run(
        [nvcc, "--ptx", "-std=c++17", f"-arch={arch}", str(kernel_src), "-o", str(ptx_path)],
        check=True,
        capture_output=True,
        text=True,
    )
    return ptx_path.read_bytes(), f"nvcc-slow-{arch}"


def _check_cuda(rc: int, op: str) -> None:
    if rc != 0:
        raise RuntimeError(f"{op} failed with CUDA driver code {rc}")


def _check_cublas(rc: int, op: str) -> None:
    if rc != 0:
        raise RuntimeError(f"{op} failed with cuBLAS status {rc}")


def _load_shared_library(names: tuple[str, ...]) -> ctypes.CDLL:
    last_error: OSError | None = None
    for name in names:
        try:
            return ctypes.CDLL(name)
        except OSError as exc:
            last_error = exc
    joined = ", ".join(names)
    raise RuntimeError(f"unable to load any of: {joined}") from last_error


