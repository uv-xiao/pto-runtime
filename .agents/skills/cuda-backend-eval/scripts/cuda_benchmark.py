def _check_runtime(rc: int, op: str) -> None:
    if rc != 0:
        raise RuntimeError(f"{op} failed")


def _run_pto_prepared(runtime, ctx: int, callable_id: int, args: CudaVectorAddArgs) -> None:
    timing = PtoRunTiming()
    _check_runtime(
        runtime.run_prepared(
            ctx,
            None,
            callable_id,
            ctypes.byref(args),
            1,
            0,
            0,
            0,
            0,
            0,
            None,
            ctypes.byref(timing),
        ),
        f"run_prepared {callable_id}",
    )


def _run_parallel_pair(run_one) -> int:
    errors: list[BaseException] = []

    def run_thread(callable_id: int) -> None:
        try:
            run_one(callable_id)
        except BaseException as exc:  # noqa: BLE001
            errors.append(exc)

    start = time.perf_counter_ns()
    threads = [threading.Thread(target=run_thread, args=(callable_id,)) for callable_id in (0, 1)]
    for thread in threads:
        thread.start()
    for thread in threads:
        thread.join()
    if errors:
        raise RuntimeError(str(errors[0]))
    return time.perf_counter_ns() - start


def _stream_result(
    machine: str,
    baseline: str,
    repeat: int,
    wall_ns: int,
    speedup: float | None = None,
) -> dict[str, Any]:
    result: dict[str, Any] = {
        "machine": machine,
        "baseline": baseline,
        "n": 2,
        "repeat": repeat,
        "host_wall_ns": wall_ns,
        "device_wall_ns": wall_ns,
        "status": "pass",
    }
    if speedup is not None:
        result["speedup_vs_serial"] = speedup
    return result


def _run_stream_concurrency_repeat(
    runtime,
    ctx: int,
    ptx_buf,
    ptx_size: int,
    machine: str,
    repeat: int,
) -> list[dict[str, Any]]:
    array_t = ctypes.c_float * 1
    host_a = array_t(1.0)
    host_b = array_t(2.0)
    host_out = [array_t(), array_t()]
    nbytes = ctypes.sizeof(host_a)
    dev_a = runtime.device_malloc_ctx(ctx, nbytes)
    dev_b = runtime.device_malloc_ctx(ctx, nbytes)
    dev_out = [runtime.device_malloc_ctx(ctx, nbytes), runtime.device_malloc_ctx(ctx, nbytes)]
    if not (dev_a and dev_b and dev_out[0] and dev_out[1]):
        raise RuntimeError("device allocation failed")
    try:
        _check_runtime(runtime.copy_to_device_ctx(ctx, dev_a, ctypes.byref(host_a), nbytes), "copy_to_device a")
        _check_runtime(runtime.copy_to_device_ctx(ctx, dev_b, ctypes.byref(host_b), nbytes), "copy_to_device b")
        manifests = [
            CudaHostCallable(
                version=2,
                op=1,
                image=ctypes.cast(ptx_buf, ctypes.c_void_p),
                image_size=ptx_size,
                entry_name=b"pto_vector_add_f32",
                grid_dim=1,
                block_dim=1,
                shared_mem_bytes=0,
                stream_id=stream_id,
            )
            for stream_id in (0, 1)
        ]
        for callable_id, manifest in enumerate(manifests):
            _check_runtime(runtime.prepare_callable(ctx, callable_id, ctypes.byref(manifest)), f"prepare {callable_id}")

        args = [
            CudaVectorAddArgs(a=dev_a, b=dev_b, out=dev_out[0], n=1),
            CudaVectorAddArgs(a=dev_a, b=dev_b, out=dev_out[1], n=1),
        ]

        serial_start = time.perf_counter_ns()
        _run_pto_prepared(runtime, ctx, 0, args[0])
        _run_pto_prepared(runtime, ctx, 1, args[1])
        serial_wall_ns = time.perf_counter_ns() - serial_start
        parallel_wall_ns = _run_parallel_pair(
            lambda callable_id: _run_pto_prepared(runtime, ctx, callable_id, args[callable_id])
        )

        for idx in range(2):
            _check_runtime(
                runtime.copy_from_device_ctx(ctx, ctypes.byref(host_out[idx]), dev_out[idx], nbytes),
                f"copy {idx}",
            )
            if list(host_out[idx]) != [3.0]:
                raise RuntimeError(f"stream output {idx} mismatch")
            _check_runtime(runtime.unregister_callable(ctx, idx), f"unregister {idx}")

        return [
            _stream_result(machine, "pto_stream_serial", repeat, serial_wall_ns),
            _stream_result(
                machine,
                "pto_stream_parallel",
                repeat,
                parallel_wall_ns,
                speedup=serial_wall_ns / parallel_wall_ns,
            ),
        ]
    finally:
        runtime.device_free_ctx(ctx, dev_a)
        runtime.device_free_ctx(ctx, dev_b)
        runtime.device_free_ctx(ctx, dev_out[0])
        runtime.device_free_ctx(ctx, dev_out[1])


def run_stream_concurrency_benchmark(
    device: int, repeats: int, arch: str, label: str, stream_pool_size: int | None = None
) -> dict[str, Any]:
    with tempfile.TemporaryDirectory(prefix="pto_cuda_stream_") as td:
        ptx, ptx_source = _compile_slow_ptx(Path(td), arch)
    ptx_buf = ctypes.create_string_buffer(ptx + b"\0")
    runtime, binaries = _load_runtime(True)

    metadata = {
        "label": label,
        "machine": socket.gethostname(),
        "git_commit": _git_commit(),
        "timestamp_unix": time.time(),
        "device": device,
        "block_dim": 1,
        "ptx_arch": arch,
        "ptx_source": ptx_source,
        "nvidia_smi": _nvidia_smi_summary(),
        "paper_setup": (
            "CUDA stream-concurrency microbenchmark for host_schedule; not an end-to-end LLM serving result."
        ),
        "source_papers": list(SOURCE_PAPERS),
        "host_runtime": str(binaries.host_path),
    }
    if stream_pool_size is not None:
        metadata["stream_pool_size"] = stream_pool_size

    results: list[dict[str, Any]] = []
    previous_stream_pool_size = os.environ.get("PTO_CUDA_STREAM_POOL_SIZE")
    if stream_pool_size is not None:
        os.environ["PTO_CUDA_STREAM_POOL_SIZE"] = str(stream_pool_size)
    try:
        for repeat in range(repeats):
            ctx = runtime.create_device_context()
            if not ctx:
                raise RuntimeError("create_device_context returned null")
            try:
                if runtime.simpler_init(ctx, device, None, 0, None, 0) != 0:
                    raise RuntimeError("simpler_init failed")
                results.extend(
                    _run_stream_concurrency_repeat(runtime, ctx, ptx_buf, len(ptx) + 1, metadata["machine"], repeat)
                )
            finally:
                runtime.finalize_device(ctx)
                runtime.destroy_device_context(ctx)
    finally:
        if stream_pool_size is not None:
            if previous_stream_pool_size is None:
                os.environ.pop("PTO_CUDA_STREAM_POOL_SIZE", None)
            else:
                os.environ["PTO_CUDA_STREAM_POOL_SIZE"] = previous_stream_pool_size

    return {"metadata": metadata, "results": results}


def summarize_results(payload: dict[str, Any]) -> dict[tuple[str, str, int, int, int], dict[str, Any]]:
    grouped: dict[tuple[str, str, int, int, int], list[dict[str, Any]]] = defaultdict(list)
    for row in payload.get("results", []):
        key = (
            row["machine"],
            row["baseline"],
            int(row["n"]),
            int(row.get("task_count", 1)),
            int(row.get("worker_blocks_per_task", 1)),
        )
        grouped[key].append(row)

    summary: dict[tuple[str, str, int, int, int], dict[str, Any]] = {}
    for key, rows in grouped.items():
        host_values = [int(row.get("host_wall_ns", row["device_wall_ns"])) for row in rows]
        device_values = [int(row["device_wall_ns"]) for row in rows]
        summary[key] = {
            "machine": key[0],
            "baseline": key[1],
            "n": key[2],
            "task_count": key[3],
            "worker_blocks_per_task": key[4],
            "samples": len(rows),
            "median_host_wall_ns": int(statistics.median(host_values)),
            "median_device_wall_ns": int(statistics.median(device_values)),
        }
    return summary


