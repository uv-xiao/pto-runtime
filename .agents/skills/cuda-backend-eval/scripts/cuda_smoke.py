def _find_nvcc() -> str | None:
    for env_name in ("CUDA_HOME", "CUDA_PATH"):
        cuda_root = os.environ.get(env_name)
        if not cuda_root:
            continue
        nvcc = Path(cuda_root) / "bin" / "nvcc"
        if nvcc.is_file():
            return str(nvcc)

    nvcc = shutil.which("nvcc")
    if nvcc:
        return nvcc

    for nvcc in sorted(Path("/usr/local").glob("cuda*/bin/nvcc"), reverse=True):
        if nvcc.is_file():
            return str(nvcc)
    return None


class PtoRunTiming(ctypes.Structure):
    _fields_ = [
        ("host_wall_ns", ctypes.c_uint64),
        ("device_wall_ns", ctypes.c_uint64),
    ]


def _compile_ptx(work_dir: Path, arch: str) -> tuple[bytes, str]:
    nvcc = _find_nvcc()
    if nvcc is None:
        return _FALLBACK_VECTOR_ADD_PTX, "embedded-sm80-ptx"

    kernel_src = work_dir / "vector_add.cu"
    kernel_src.write_text(
        """
extern "C" __global__ void pto_vector_add_f32(
    const float *a, const float *b, float *out, unsigned long long n) {
    unsigned long long i = blockIdx.x * blockDim.x + threadIdx.x;
    if (i < n) {
        out[i] = a[i] + b[i];
    }
}
""".lstrip()
    )
    ptx_path = work_dir / "vector_add.ptx"
    subprocess.run(
        [nvcc, "--ptx", "-std=c++17", f"-arch={arch}", str(kernel_src), "-o", str(ptx_path)],
        check=True,
        capture_output=True,
        text=True,
    )
    return ptx_path.read_bytes(), f"nvcc-{arch}"


def _bind_runtime(runtime):
    runtime.create_device_context.restype = ctypes.c_void_p
    runtime.destroy_device_context.argtypes = [ctypes.c_void_p]
    runtime.simpler_init.argtypes = [
        ctypes.c_void_p,
        ctypes.c_int,
        ctypes.c_void_p,
        ctypes.c_size_t,
        ctypes.c_void_p,
        ctypes.c_size_t,
    ]
    runtime.simpler_init.restype = ctypes.c_int
    runtime.finalize_device.argtypes = [ctypes.c_void_p]
    runtime.finalize_device.restype = ctypes.c_int
    runtime.device_malloc_ctx.argtypes = [ctypes.c_void_p, ctypes.c_size_t]
    runtime.device_malloc_ctx.restype = ctypes.c_void_p
    runtime.device_free_ctx.argtypes = [ctypes.c_void_p, ctypes.c_void_p]
    runtime.copy_to_device_ctx.argtypes = [ctypes.c_void_p, ctypes.c_void_p, ctypes.c_void_p, ctypes.c_size_t]
    runtime.copy_to_device_ctx.restype = ctypes.c_int
    runtime.copy_from_device_ctx.argtypes = [ctypes.c_void_p, ctypes.c_void_p, ctypes.c_void_p, ctypes.c_size_t]
    runtime.copy_from_device_ctx.restype = ctypes.c_int
    runtime.prepare_callable.argtypes = [ctypes.c_void_p, ctypes.c_int32, ctypes.c_void_p]
    runtime.prepare_callable.restype = ctypes.c_int
    runtime.run_prepared.argtypes = [
        ctypes.c_void_p,
        ctypes.c_void_p,
        ctypes.c_int32,
        ctypes.c_void_p,
        ctypes.c_int,
        ctypes.c_int,
        ctypes.c_int,
        ctypes.c_int,
        ctypes.c_int,
        ctypes.c_int,
        ctypes.c_char_p,
        ctypes.POINTER(PtoRunTiming),
    ]
    runtime.run_prepared.restype = ctypes.c_int
    runtime.unregister_callable.argtypes = [ctypes.c_void_p, ctypes.c_int32]
    runtime.unregister_callable.restype = ctypes.c_int


@functools.lru_cache(maxsize=2)
def _load_runtime(build: bool):
    binaries = RuntimeBuilder(platform="cuda").get_binaries("host_schedule", build=build)
    runtime = ctypes.CDLL(str(binaries.host_path))
    _bind_runtime(runtime)
    return runtime, binaries


def _worker_task_body(op: str) -> str:
    if op == "add":
        expression = "ctx->a[i] + ctx->b[i]"
    elif op == "mul":
        expression = "ctx->a[i] * ctx->b[i]"
    elif op == "scale":
        expression = "ctx->a[i] * ctx->alpha"
    elif op == "square":
        expression = "ctx->a[i] * ctx->a[i]"
    elif op == "axpy":
        expression = "ctx->alpha * ctx->a[i] + ctx->b[i]"
    elif op == "affine":
        expression = "ctx->alpha * ctx->a[i] + ctx->beta * ctx->b[i]"
    elif op == "triad":
        expression = "ctx->a[i] * ctx->b[i] + ctx->c[i]"
    elif op == "quad":
        expression = "ctx->a[i] * ctx->b[i] + ctx->c[i] * ctx->d[i]"
    elif op == "generic_args":
        expression = "ctx->scalar0 * ctx->a[i] + ctx->tensor0[i] + ctx->scalar1 * ctx->tensor1[i] + ctx->b[i]"
    elif op == "generic_args4":
        expression = (
            "ctx->scalar0 * ctx->a[i] + ctx->tensor0[i] + "
            "ctx->scalar1 * ctx->tensor1[i] + ctx->scalar2 * ctx->tensor2[i] + "
            "ctx->scalar3 * ctx->tensor3[i] + ctx->b[i]"
        )
    else:
        raise ValueError(f"unknown worker smoke op: {op}")
    return f"""
unsigned long long i = blockIdx.x * blockDim.x + threadIdx.x;
if (i < ctx->n) {{
    ctx->out[i] = {expression};
}}
""".lstrip()


def _float32(value: float) -> float:
    return ctypes.c_float(value).value


def _fma_f32(a: float, b: float, c: float) -> float:
    return _float32(float(a) * float(b) + float(c))


def _worker_expected_output(op: str, n: int) -> list[float]:
    if op == "add":
        return [float(3 * i) for i in range(n)]
    if op == "mul":
        return [float(i * (2 * i)) for i in range(n)]
    if op == "scale":
        return [float(i * 1.5) for i in range(n)]
    if op == "square":
        return [_float32(_float32(float(i)) * _float32(float(i))) for i in range(n)]
    if op == "axpy":
        return [float(1.5 * i + (2 * i)) for i in range(n)]
    if op == "affine":
        return [float(1.5 * i + 0.5 * (2 * i)) for i in range(n)]
    if op == "triad":
        return [float(i * (2 * i) + (3 * i)) for i in range(n)]
    if op == "quad":
        return [_fma_f32(float(i), float(2 * i), _float32(float(3 * i) * float(4 * i))) for i in range(n)]
    if op == "generic_args":
        return [float(7.5 * i) for i in range(n)]
    if op == "generic_args4":
        return [float(8.5 * i) for i in range(n)]
    raise ValueError(f"unknown worker smoke op: {op}")


