def _worker_raw_args(op: str, dev_a, dev_b, dev_c, dev_d, dev_e, dev_f, dev_out, n: int):
    if op == "scale":
        return CudaVectorScaleArgs(a=dev_a, out=dev_out, alpha=1.5, n=n)
    if op == "square":
        return CudaVectorUnaryArgs(a=dev_a, out=dev_out, n=n)
    if op == "axpy":
        return CudaVectorAxpyArgs(a=dev_a, b=dev_b, out=dev_out, alpha=1.5, n=n)
    if op == "affine":
        return CudaVectorAffineArgs(a=dev_a, b=dev_b, out=dev_out, alpha=1.5, beta=0.5, n=n)
    if op == "triad":
        return CudaVectorTernaryArgs(a=dev_a, b=dev_b, c=dev_c, out=dev_out, n=n)
    if op == "quad":
        return CudaVectorQuaternaryArgs(a=dev_a, b=dev_b, c=dev_c, d=dev_d, out=dev_out, n=n)
    if op == "generic_args":
        tensor_args_t = ctypes.c_void_p * 4
        scalar_args_t = ctypes.c_float * 4
        return CudaVectorGenericArgs(
            a=dev_a,
            b=dev_b,
            out=dev_out,
            tensor_args=tensor_args_t(dev_c, dev_d, 0, 0),
            scalar_args=scalar_args_t(1.5, 0.25, 0.0, 0.0),
            tensor_arg_count=2,
            scalar_arg_count=2,
            n=n,
        )
    if op == "generic_args4":
        tensor_args_t = ctypes.c_void_p * 4
        scalar_args_t = ctypes.c_float * 4
        return CudaVectorGenericArgs(
            a=dev_a,
            b=dev_b,
            out=dev_out,
            tensor_args=tensor_args_t(dev_c, dev_d, dev_e, dev_f),
            scalar_args=scalar_args_t(1.5, 0.25, 0.125, 0.0625),
            tensor_arg_count=4,
            scalar_arg_count=4,
            n=n,
        )
    return CudaVectorAddArgs(a=dev_a, b=dev_b, out=dev_out, n=n)


def run_smoke(device: int, n: int, block_dim: int, arch: str, build: bool = True) -> dict:
    with tempfile.TemporaryDirectory(prefix="pto_cuda_smoke_") as td:
        ptx, ptx_source = _compile_ptx(Path(td), arch)
    ptx_buf = ctypes.create_string_buffer(ptx + b"\0")

    runtime, binaries = _load_runtime(build)

    ctx = runtime.create_device_context()
    if not ctx:
        raise RuntimeError("create_device_context returned null")

    start = time.time()
    try:
        if runtime.simpler_init(ctx, device, None, 0, None, 0) != 0:
            raise RuntimeError("simpler_init failed")

        array_t = ctypes.c_float * n
        host_a = array_t(*[float(i) for i in range(n)])
        host_b = array_t(*[float(2 * i) for i in range(n)])
        host_out = array_t()
        nbytes = ctypes.sizeof(host_a)

        dev_a = runtime.device_malloc_ctx(ctx, nbytes)
        dev_b = runtime.device_malloc_ctx(ctx, nbytes)
        dev_out = runtime.device_malloc_ctx(ctx, nbytes)
        if not (dev_a and dev_b and dev_out):
            raise RuntimeError("device allocation failed")

        try:
            if runtime.copy_to_device_ctx(ctx, dev_a, ctypes.byref(host_a), nbytes) != 0:
                raise RuntimeError("copy_to_device a failed")
            if runtime.copy_to_device_ctx(ctx, dev_b, ctypes.byref(host_b), nbytes) != 0:
                raise RuntimeError("copy_to_device b failed")

            manifest = CudaHostCallable(
                version=2,
                op=1,
                image=ctypes.cast(ptx_buf, ctypes.c_void_p),
                image_size=len(ptx) + 1,
                entry_name=b"pto_vector_add_f32",
                grid_dim=(n + block_dim - 1) // block_dim,
                block_dim=block_dim,
                shared_mem_bytes=0,
                stream_id=0,
            )
            args = CudaVectorAddArgs(a=dev_a, b=dev_b, out=dev_out, n=n)
            timing = PtoRunTiming()

            if runtime.prepare_callable(ctx, 0, ctypes.byref(manifest)) != 0:
                raise RuntimeError("prepare_callable failed")
            if (
                runtime.run_prepared(
                    ctx, None, 0, ctypes.byref(args), block_dim, 0, 0, 0, 0, 0, None, ctypes.byref(timing)
                )
                != 0
            ):
                raise RuntimeError("run_prepared failed")
            if runtime.copy_from_device_ctx(ctx, ctypes.byref(host_out), dev_out, nbytes) != 0:
                raise RuntimeError("copy_from_device failed")
            expected = [float(3 * i) for i in range(n)]
            if list(host_out) != expected:
                raise RuntimeError("vector-add output mismatch")
            if runtime.unregister_callable(ctx, 0) != 0:
                raise RuntimeError("unregister_callable failed")
        finally:
            runtime.device_free_ctx(ctx, dev_a)
            runtime.device_free_ctx(ctx, dev_b)
            runtime.device_free_ctx(ctx, dev_out)
    finally:
        runtime.finalize_device(ctx)
        runtime.destroy_device_context(ctx)

    return {
        "status": "pass",
        "device": device,
        "n": n,
        "block_dim": block_dim,
        "ptx_arch": arch,
        "ptx_source": ptx_source,
        "host_wall_ns": timing.host_wall_ns,
        "device_wall_ns": timing.device_wall_ns,
        "elapsed_s": time.time() - start,
        "host_runtime": str(binaries.host_path),
    }


def run_worker_smoke(device: int, n: int, block_dim: int, arch: str, build: bool = True, op: str = "add") -> dict:
    with tempfile.TemporaryDirectory(prefix="pto_cuda_worker_smoke_") as td:
        work_dir = Path(td)
        task_src = work_dir / f"vector_{op}.pto.cu"
        task_src.write_text(_worker_task_body(op))
        artifact = KernelCompiler(platform="cuda").compile_cuda_host_schedule(
            str(task_src),
            task_name=f"vector_{op}_worker_smoke",
            arch=arch,
            cache_root=work_dir / "cache",
            context_definition=_worker_context_definition(op),
            host_parameters=_worker_host_parameters(op),
            host_context_initializer=_worker_host_context_initializer(op),
        )

    prepared = prepare_cuda_host_schedule_callable(
        artifact,
        grid_dim=(n + block_dim - 1) // block_dim,
        block_dim=block_dim,
        op=_worker_host_op(op),
    )

    worker = Worker(level=2, device_id=device, platform="cuda", runtime="host_schedule", build=build)
    worker.init()
    cid = worker.register(prepared)
    start = time.time()
    try:
        array_t = ctypes.c_float * n
        host_a = array_t(*[float(i) for i in range(n)])
        host_b = array_t(*[float(2 * i) for i in range(n)])
        host_c = array_t(*[float(3 * i) for i in range(n)])
        host_e = array_t(*[float(5 * i) for i in range(n)])
        host_f = array_t(*[float(6 * i) for i in range(n)])
        host_out = array_t()
        nbytes = ctypes.sizeof(host_a)

        dev_a = worker.malloc(nbytes)
        dev_b = worker.malloc(nbytes) if op not in {"scale", "square"} else None
        dev_c = worker.malloc(nbytes) if op in {"triad", "quad", "generic_args", "generic_args4"} else None
        dev_d = worker.malloc(nbytes) if op in {"quad", "generic_args", "generic_args4"} else None
        dev_e = worker.malloc(nbytes) if op == "generic_args4" else None
        dev_f = worker.malloc(nbytes) if op == "generic_args4" else None
        dev_out = worker.malloc(nbytes)
        try:
            worker.copy_to(dev_a, ctypes.addressof(host_a), nbytes)
            if dev_b is not None:
                worker.copy_to(dev_b, ctypes.addressof(host_b), nbytes)
            if dev_c is not None:
                worker.copy_to(dev_c, ctypes.addressof(host_c), nbytes)
            if dev_d is not None:
                host_d = array_t(*[float(4 * i) for i in range(n)])
                worker.copy_to(dev_d, ctypes.addressof(host_d), nbytes)
            if dev_e is not None:
                worker.copy_to(dev_e, ctypes.addressof(host_e), nbytes)
            if dev_f is not None:
                worker.copy_to(dev_f, ctypes.addressof(host_f), nbytes)

            args = _worker_raw_args(op, dev_a, dev_b, dev_c, dev_d, dev_e, dev_f, dev_out, n)
            config = CallConfig()
            config.block_dim = block_dim
            timing = worker.run(cid, args, config)
            worker.copy_from(ctypes.addressof(host_out), dev_out, nbytes)
            expected = _worker_expected_output(op, n)
            if list(host_out) != expected:
                raise RuntimeError(f"vector-{op} output mismatch")
        finally:
            worker.free(dev_a)
            if dev_b is not None:
                worker.free(dev_b)
            if dev_c is not None:
                worker.free(dev_c)
            if dev_d is not None:
                worker.free(dev_d)
            if dev_e is not None:
                worker.free(dev_e)
            if dev_f is not None:
                worker.free(dev_f)
            worker.free(dev_out)
    finally:
        worker.close()

    return {
        "status": "pass",
        "runner": "worker",
        "runtime": "host_schedule",
        "mode": f"worker/{op}",
        "op": op,
        "device": device,
        "n": n,
        "block_dim": block_dim,
        "ptx_arch": arch,
        "ptx_source": (
            f"kernel-compiler-worker-task-body-{arch}"
            if op == "add"
            else f"kernel-compiler-worker-task-body-{op}-{arch}"
        ),
        "host_wall_ns": timing.host_wall_ns,
        "device_wall_ns": timing.device_wall_ns,
        "elapsed_s": time.time() - start,
        "host_runtime": str(RuntimeBuilder(platform="cuda").get_binaries("host_schedule", build=False).host_path),
    }


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--device", type=int, default=0)
    parser.add_argument("--n", type=int, default=1024)
    parser.add_argument("--block-dim", type=int, default=256)
    parser.add_argument("--arch", default="compute_80")
    parser.add_argument("--runner", choices=("direct_c_api", "worker"), default="direct_c_api")
    parser.add_argument(
        "--op",
        choices=("add", "mul", "scale", "square", "axpy", "affine", "triad", "quad", "generic_args", "generic_args4"),
        default="add",
        help="Worker task body operation",
    )
    parser.add_argument("--no-build", action="store_true", help="Use existing runtime binaries without rebuilding")
    parser.add_argument("--output-json", type=Path, help="Optional path to write the smoke JSON payload")
    args = parser.parse_args()

    if args.runner == "worker":
        result = run_worker_smoke(args.device, args.n, args.block_dim, args.arch, build=not args.no_build, op=args.op)
    else:
        if args.op != "add":
            parser.error("--op is only supported by --runner worker")
        result = run_smoke(args.device, args.n, args.block_dim, args.arch, build=not args.no_build)
    rendered = json.dumps(result, indent=2, sort_keys=True)
    if args.output_json is not None:
        args.output_json.parent.mkdir(parents=True, exist_ok=True)
        args.output_json.write_text(rendered + "\n")
    print(rendered)


if __name__ == "__main__":
    main()
