def main() -> int:
    parser = argparse.ArgumentParser(description="Generate PTO2 scheduling report (Markdown)")
    parser.add_argument(
        "perf",
        nargs="?",
        help="perf_swimlane_*.json path (default: latest in outputs/)",
    )
    parser.add_argument(
        "-k",
        "--kernel-config",
        default=None,
        help="kernel_config.py (optional, for func_id->name mapping)",
    )
    parser.add_argument(
        "--log",
        default=None,
        help="log file path containing PTO2_SCHED_PROFILE_JSON lines (optional)",
    )
    parser.add_argument(
        "-o",
        "--output",
        default=None,
        help="output Markdown path (default: outputs/pto2_schedule_report_<timestamp>.md)",
    )

    args = parser.parse_args()

    perf_path = Path(args.perf) if args.perf else _find_latest_outputs_file("perf_swimlane_*.json")
    perf = _load_json(perf_path)
    tasks = _parse_tasks(perf)

    func_map = _load_kernel_config_mapping(Path(args.kernel_config) if args.kernel_config else None)

    log_path = Path(args.log) if args.log else None
    sched_profiles: List[Dict[str, Any]] = []
    # Prefer scheduler profiles embedded in perf_swimlane JSON (works on real hardware).
    embedded = perf.get("scheduler_profiles")
    if isinstance(embedded, list) and embedded:
        sched_profiles = [p for p in embedded if isinstance(p, dict)]
    elif log_path is not None:
        profiles_with_lineno = _parse_scheduler_profiles_with_lineno(log_path)
        sched_profiles = _pick_last_run_profiles(profiles_with_lineno, log_path, perf_path.name)

    if args.output:
        out_path = Path(args.output)
    else:
        ts = datetime.now().strftime("%Y%m%d_%H%M%S")
        out_path = Path("outputs") / f"pto2_schedule_report_{ts}.md"

    _write_report(
        out_path=out_path,
        perf_path=perf_path,
        tasks=tasks,
        func_map=func_map,
        log_path=log_path,
        sched_profiles=sched_profiles,
    )
    print(f"[OK] Wrote report: {out_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
